namespace gamadev.xtable
{
    //
    // Summary:
    //     Defines constants that specify the visibility of the grid lines in a XTable.
    public enum XTableGridLinesVisibility
    {
        //
        // Summary:
        //     Both horizontal and vertical grid lines are visible.
        All = 0,

        //
        // Summary:
        //     Only horizontal grid lines are visible.
        Horizontal = 1,

        //
        // Summary:
        //     No grid lines are visible.
        None = 2,

        //
        // Summary:
        //     Only vertical grid lines are visible.
        Vertical = 3
    }
}